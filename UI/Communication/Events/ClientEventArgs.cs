using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace UI.Communication.Events
{
    /// <summary>
    /// EventArgs for all active clients
    /// </summary>
    public class ClientEventArgs: System.EventArgs
    {
        public readonly string[] usernames;

        public ClientEventArgs(string[] usernames)
        {
            this.usernames = usernames;
        }
    }
}
