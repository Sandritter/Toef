using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
using UI.Model;

namespace UI.Communication.Events
{
    /// <summary>
    /// Event arguments which are used after participants received
    /// </summary>
    public class ParticipantEventArgs: System.EventArgs
    {
        public readonly Dictionary<int, Participant> participantArray;

        public ParticipantEventArgs(Dictionary<int, Participant> participantArray)
        {
            this.participantArray = participantArray;
        }
    }
}
