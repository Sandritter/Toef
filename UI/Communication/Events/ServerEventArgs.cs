using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace UI.Communication.Events
{
    /// <summary>
    /// Event arguments which are used if the list of all server received
    /// </summary>
    public class ServerEventArgs: System.EventArgs
    {
        public readonly string[] list;

        public ServerEventArgs(string[] list)
        {
            this.list = list;
        }
    }
}
